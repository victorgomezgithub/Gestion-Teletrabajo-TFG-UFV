package com.gestionTeletrabajo.SpringBoot.bigData;


public class ChartDataSets {

	private int year;
	
	private int month;
	
	private int total;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	
}
